package standardjava;

public class Operator2 {
	
 public static void main(String[] args) {
	 
	 int a = 33;   // operator
	 
	 System.out.println(a);     // 33
	  System.out.println(a--); //  33(33-1)
	  System.out.println(a--); //  32(32-1)
	  System.out.println(a);   //  31
	  System.out.println(a--); //  31(31-1)
	  System.out.println(a--); //  30(30-1)
	  System.out.println(a--); //  29(29-1)
	  System.out.println(a);  //   28
	  System.out.println(a--); //  28(28-1)
	  System.out.println(a); //    27

	 
	 
	 
	 
	 
 }

}
