package standardjava;

public class Operator3 {
	
	
	public static void main(String[] args) {
		
		  int a = -35; // bitwise operator//
		  
		  System.out.println (a); //(-35)//
		  System.out.println (~a);//-(-35+1)=34
		  System.out.println (~a);//-(-35+1)=34
		  System.out.println (~a);//-(-35+1)=34
		  System.out.println (a);//-35
		  System.out.println (~a);//-(-35+1)=34
		  System.out.println (a);//-35//  
 }
	

}
